# Name Cases: Use a variable to represent a person’s name, and then print that person’s name in lowercase, uppercase, and title case

lower_name: str = "Allama Iqbal"
print(lower_name.lower()) # allama iqbal

upper_name: str = "allama iqbal" 
print(upper_name.upper()) # ALLAMA IQBAL

title_name: str = "allAmA iQbaL"
print(title_name.title()) # Allama Iqbal